public class Event {
    private String name;

    /**
     * Creates a new Event with the given name
     *
     * @param name the name of the event
     */
    public Event(String name) {
        this.name = name;
    }

    /**
     * Gets the name of the event.
     *
     * @return the name of the event
     */
    public String getName() {
        return name;
    }

}
